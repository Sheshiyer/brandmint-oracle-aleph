class TestFilterUploadableDocuments:
    """Test document filtering by uploadability."""

    def test_filters_non_uploadable(self):
        docs = {
            "doc1": "High quality content with sufficient length and no placeholders or issues. This document contains comprehensive information that meets all quality standards and should be considered uploadable. The content is well-written and provides value to readers.",
            "doc2": "TODO: Incomplete.",
        }
        weights = CurationWeights(min_uploadable_score=60.0)
        scores = calculate_curation_scores(docs, weights=weights)
        filtered = filter_uploadable_documents(docs, scores)
        # doc1 should be uploadable with adjusted threshold
        assert "doc1" in filtered

    def test_empty_documents(self):
        filtered = filter_uploadable_documents({}, {})
        assert filtered == {}


class TestGenerateCurationReport:
    """Test report generation."""

    def test_generates_report_with_suppressed_docs(self):
        scores = {
            "doc1": CurationScore(doc_id="doc1", base_score=80, placeholder_penalty=5, meta_penalty=0, final_score=75, is_uploadable=True),
            "doc2": CurationScore(
                doc_id="doc2",
                base_score=30,
                placeholder_penalty=40,
                meta_penalty=20,
                final_score=20,
                is_uploadable=False,
                suppression_reasons=["Score too low"],
            ),
        }
        report = generate_curation_report(scores)
        assert "# Curation Scoring Report" in report
        assert "Total documents: 2" in report
        assert "Uploadable: 1" in report
        assert "Suppressed: 1" in report
        assert "## Suppressed Documents" in report
        assert "### doc2" in report

    def test_generates_report_with_no_suppressed_docs(self):
        scores = {
            "doc1": CurationScore(doc_id="doc1", base_score=80, placeholder_penalty=5, meta_penalty=0, final_score=75, is_uploadable=True),
        }
        report = generate_curation_report(scores)
        assert "# Curation Scoring Report" in report
        assert "Suppressed: 0" in report
        assert "## Suppressed Documents" not in report


class TestGetWeightsForProfile:
    """Test weight presets for profiles."""

    def test_brand_public_weights(self):
        weights = get_weights_for_profile("brand-public")
        assert weights.min_uploadable_score == 75.0
        assert weights.placeholder_penalty_multiplier == 2.0

    def test_strategy_internal_weights(self):
        weights = get_weights_for_profile("strategy-internal")
        assert weights.min_uploadable_score == 60.0
        assert weights.placeholder_penalty_multiplier == 1.5

    def test_kickstarter_conditional_weights(self):
        weights = get_weights_for_profile("kickstarter-conditional")
        assert weights.min_uploadable_score == 85.0
        assert weights.placeholder_penalty_multiplier == 3.0

    def test_debug_internal_weights(self):
        weights = get_weights_for_profile("debug-internal")
        assert weights.min_uploadable_score == 0.0
        assert weights.placeholder_penalty_multiplier == 0.5

    def test_unknown_profile_defaults_to_brand_public(self):
        weights = get_weights_for_profile("unknown")
        brand_public_weights = get_weights_for_profile("brand-public")
        assert weights.min_uploadable_score == brand_public_weights.min_uploadable_score
